Plugins[pluginName].Load = function()
	notification("Version " .. Plugins[pluginName]:GetVersion() .. " " .. Strings.PluginText);
end
------------------------------------------------------------------------------------------
-- Unload Plugin --
------------------------------------------------------------------------------------------
Plugins[pluginName].Unload = function()	

	SaveSettings();

	notification("Unactivated");
end